/*
ASSIGNMENT OPERATORS
*/
let r = 5
console.log(r);
// our equal sign is the assignment

//addition assignment
r += 1; //r= r+1 or 5 + 1 in this case
console.log(r);

//subtraction

r -= 2; // r = r - 2
console.log(r);

//multiplication
r *= 2; //r = * 2
console.log(r);

//division
r /= 1; //r = r/1
console.log(r);

//EXPONENTIAL ASSIGNMENT
r **= 3; //r = r
console.log(r); // r * r * r

//REMAINDER (MODULUS) ASSIGNment
r %= 1; //r = r % 1
console.log(r);

let y = 5;
console.log(y);
y %= 2;
console.log(y);